# Python concatenation
# This simply means joining two or more string values together
firstName = "Genny"
lastName = "McKinsen"
fullName = firstName + lastName

print(fullName)                   # GennyMcKinson
print(firstName + " " + lastName) # Genny McKinsen

# Formatted String
print(f'My name is {firstName} {lastName}') # AMy name is Genny McKinsen