# Python logical operators [ and or not]
# Boolean operators are used to combine conditional statements


# and: Returns True when both conditions are true
x = 10
y = 5
z = 10

print(x == y and y < z) # False
print(x == z and y < z) # True

# or # Returns True when one of the two conditions is true
a = 7
b = 4
c = 10

print(a == b or b > a) # False 
print(a <= b or b < c) # True 

# not
p = 10
q = 12
r = 15

print(not(p==r)) # True


